
public class ArraySum 
{
	private long singleThreadArraySum;
	private long firstThreadArraySum;
	private long secondThreadArraySum;
	
	public long getSingleThreadArraySum() 
	{
		return singleThreadArraySum;
	}
	public void setSingleThreadArraySum(long singleThreadArraySum) 
	{
		this.singleThreadArraySum = singleThreadArraySum;
	}
	public long getFirstThreadArraySum() 
	{
		return firstThreadArraySum;
	}
	public void setFirstThreadArraySum(long firstThreadArraySum) 
	{
		this.firstThreadArraySum = firstThreadArraySum;
	}
	public long getSecondThreadArraySum() 
	{
		return secondThreadArraySum;
	}
	public void setSecondThreadArraySum(long secondThreadArraySum) 
	{
		this.secondThreadArraySum = secondThreadArraySum;
	}


}
